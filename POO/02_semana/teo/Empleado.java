package semana2.teo;

public class Empleado {

    String nombre;
    String rango;
    int dni;
    double sueldo;

    public Empleado(){

    }

    public Empleado(String nombre, String rango, int dni, double sueldo){
        this.nombre = nombre;
        this.rango = rango;
        this.dni = dni;
        this.sueldo = sueldo;
    }
}
